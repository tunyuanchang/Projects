        # lth layer: (RELU -> LINEAR) gradients.
        # Inputs: "dA_prev". Outputs: "dA_prev"
        for l in range(L-2, -1, -1):
          dZ = self.activation[l].backward(dA=dA_prev)
          dA_prev = self.linear[l].backward(dZ)
        ### END CODE HERE ###

        return dA_prev

