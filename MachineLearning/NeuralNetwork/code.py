            ### START CODE HERE ### (‚âà 3 lines of code)
            Z = self.cache
            dZ = np.array(dA, copy=True) # just converting dz to a correct object.
            dZ[Z <= 0] = 0 # When z <= 0, you should set dz to 0 as well.
            ### END CODE HERE ###

